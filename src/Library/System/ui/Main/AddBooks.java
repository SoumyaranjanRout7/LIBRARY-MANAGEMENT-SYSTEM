    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            JFileChooser imageFileChooser = new JFileChooser();
            int imageChooser = imageFileChooser.showOpenDialog(null);
            imageFileChooser.setDialogTitle("Choose Image...");
            //Only Choose "png" , "jpg" , "jpeg" files

            FileNameExtensionFilter fnef = new FileNameExtensionFilter("IMAGES", "png", "jpg", "jpeg");
            imageFileChooser.setFileFilter(fnef);

            if (imageChooser == JFileChooser.APPROVE_OPTION) {
                File imageFile = imageFileChooser.getSelectedFile();
                String imageFilePath = imageFile.getAbsolutePath();
                String imageFileName = imageFile.getName();

                //Display image path in text field
                AddB00k_ImgPath.setText(imageFilePath);
                AddB00k_ImgName.setText(imageFileName);
                ImageIcon imageIcon = new ImageIcon(imageFilePath);
                //Resize Image to fit the JLabel

                Image image = imageIcon.getImage().getScaledInstance(AddBook_IMG.getWidth(), AddBook_IMG.getHeight(), Image.SCALE_SMOOTH);
                //Display Image on JLabel
                ImageIcon resizedImageIcon = new ImageIcon(image);
                AddBook_IMG.setIcon(resizedImageIcon);
            }
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(this, "Please select JPG or PNG or JPEG file format", "Image Upload Failed", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

