    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int confirm = JOptionPane.showConfirmDialog(null,
                "Do you really want to delete this book?", "Delete",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                // TODO add your handling code here:
                String isAvail = "YES";
                String IsAvail = "NO";
                String Bookid = Delete_Bid.getText();

                String DeleteQuery = "DELETE FROM `book` WHERE `BOOK ID` = ?";
                String SelectStudentQuery = "SELECT * FROM `book` WHERE `BOOK ID` = ?";

                pst = con.prepareStatement(SelectStudentQuery);
                pst.setString(1, Bookid);

                rs = pst.executeQuery();
                if (rs.next() == false) {

                } else {
                    do {
                        String Isavail = rs.getString("AVAILABILITY");

                        if (Isavail.equals(isAvail)) {
                            pst = con.prepareStatement(DeleteQuery);
                            pst.setString(1, Bookid);

                            int Delete = pst.executeUpdate();

                            if (Delete == 1) {
                                JOptionPane.showMessageDialog(null, "Book deleted Successfully");
                                Delete_Bimage.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Library/System/ui/Main/project Images/Screenshot (7).png")));
                                BookIssue_Status.setText("BOOK ISSUE STATUS");
                                Delete_Bid.setText("");
                                Delete_Bname.setText("");
                                Delete_Bcategory.setText("");
                                Delete_Bauthor.setText("");
                                Delete_Bpublisher.setText(null);
                                Delete_Bprice.setText("");
                                Delete_Bedition.setText("");
                                Delete_Bpages.setText("");
                                Delete_Brack.setText("");
                                Delete_Brow.setText("");

                            }
                        }
                        if (Isavail.equals(IsAvail)) {
                            JOptionPane.showMessageDialog(null, "Book Is issued" + "\n You can't Delete", "WARNING", JOptionPane.WARNING_MESSAGE);
                        }

                    } while (rs.next());
                }

            } catch (SQLException ex) {
                Logger.getLogger(DeleteBooks.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jButton3ActionPerformed

