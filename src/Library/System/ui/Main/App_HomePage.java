    private void Update_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_ButtonActionPerformed
        try {
            // TODO add your handling code here:
            int Restriction = Integer.parseInt(Default_Value.getText());
            int finePerDay = Integer.parseInt(Default_Value1.getText());

            String bookBorrowRestriction = String.valueOf(Restriction);
            String Fine = String.valueOf(finePerDay);

            String LibrarySettingUpdateQuery = "UPDATE `librarysetting` SET `BookwithoutRestriction`=?,`FineperDays`=?";
            pst = con.prepareStatement(LibrarySettingUpdateQuery);
            pst.setInt(1, Restriction);
            pst.setInt(2, finePerDay);

            int SettingUpdate = pst.executeUpdate();

            if (SettingUpdate == 1) {

                Default_Value.setText(bookBorrowRestriction);
                Default_Value1.setText(Fine);
                JOptionPane.showMessageDialog(null, "Setting updated Successfully", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                Default_Value.setEditable(false);
                Default_Value1.setEditable(false);
                Update_Button.setVisible(false);
            }

        } catch (SQLException ex) {
            Logger.getLogger(App_HomePage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Update_ButtonActionPerformed

