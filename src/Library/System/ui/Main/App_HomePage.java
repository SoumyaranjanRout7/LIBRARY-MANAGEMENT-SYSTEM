    public void showLibrarySetting() {

        try {
            String LibrarySettingSelectQuery = "SELECT `BookwithoutRestriction`, `FineperDays` FROM `librarysetting`";

            pst = con.prepareStatement(LibrarySettingSelectQuery);

            rs = pst.executeQuery();
            while (rs.next()) {
                int BookwithoutRestriction = rs.getInt("BookwithoutRestriction");
                int FinePerDays = rs.getInt("FineperDays");

                String PreviousValue1 = String.valueOf(BookwithoutRestriction);
                String PreviousValue2 = String.valueOf(FinePerDays);

                Default_Value.setText(PreviousValue1);
                Default_Value1.setText(PreviousValue2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(App_HomePage.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

