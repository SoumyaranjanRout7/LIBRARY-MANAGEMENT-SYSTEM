    public void DateandTime() {

        Formatter fmt = new Formatter();
        Calendar now = Calendar.getInstance();

        fmt = new Formatter();
        String CurrentMonth = String.valueOf(fmt.format("%tB", now));
        LocalTime localTime = LocalTime.now();
        DateTimeFormatter dateTimeFormatter = DateTimeFormatter.ofPattern("hh:mm a");

        String Time = String.valueOf(localTime.format(dateTimeFormatter));
        int month = now.get(Calendar.DAY_OF_MONTH);
        int year = now.get(Calendar.YEAR);
        int date = now.get(Calendar.DATE);

        String[] strDays = new String[]{"Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday"};

        String Day = strDays[now.get(Calendar.DAY_OF_WEEK) - 1];
        String Month = String.valueOf(month);
        String Year = String.valueOf(year);
        String Date = String.valueOf(date);

        Display_Time.setText(Time);
        Display_Month.setText(CurrentMonth);
        Display_Date.setText(Date);
        Display_Day.setText(Day);

    }

