    public void Connect() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/slibrary", "root", "");
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddCategory.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void IssuedBookInfo_Load() {
        String SQLQuery = "SELECT `STUDENT ID`, `STUDENT NAME`, `ROLL NO.`, `BOOK ID`, `BOOK NAME`, `BOOK AUTHOR`, `BOOK PRICE`,  `ISSUE DATE`, `RETURN DATE` FROM `issuedbook`";
        try {
            pst = con.prepareStatement(SQLQuery);
            rs = pst.executeQuery();
            jTable2.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (SQLException ex) {
            Logger.getLogger(ViewBooks.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

