    private void Return_BidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Return_BidKeyReleased
        // TODO add your handling code here:
        try {
            Date d1 = new Date();
            SimpleDateFormat s = new SimpleDateFormat("yyyy-MM-dd");
            String ToDayDate = s.format(d1);
            // TODO add your handling code here:
            String book_ID = Return_Bid.getText();
            String MyQuery = "SELECT  * FROM `issuedbook` WHERE `BOOK ID` = ?";
            String MyLibrarySettingQuery = "SELECT  `FineperDays` FROM `librarysetting`";
            pst = con.prepareStatement(MyQuery);
            pst.setString(1, book_ID);
            rs = pst.executeQuery();

            if (rs.next() == false) {
                return;
            } else {
                do {
                    String StudentId = rs.getString("STUDENT ID");
                    String Sname = rs.getString("STUDENT NAME");
                    String Sroll = rs.getString("ROLL NO.");
                    String Sstream = rs.getString("IssuedStudentStream");
                    String Sdepartment = rs.getString("IssuedStudentDepartment");
                    byte[] issueStudent_img = rs.getBytes("IssuedStudentImage");

                    ImageIcon format;
                    format = new ImageIcon(issueStudent_img);
                    Image image = format.getImage().getScaledInstance(Return_Simage.getWidth(), Return_Simage.getHeight(), Image.SCALE_SMOOTH);
                    ImageIcon ResizedImageIcon = new ImageIcon(image);

                    String bookname = rs.getString("BOOK NAME");
                    String bookauthor = rs.getString("BOOK AUTHOR");
                    int PRICE = rs.getInt("BOOK PRICE");
                    String bookprice = String.valueOf(PRICE);
                    String bookpublisher = rs.getString("IssuedBookPublisher");
                    Date Issue_date = rs.getDate("ISSUE DATE");
                    String R_IssueDate = String.valueOf(Issue_date);
                    String bookedition = rs.getString("IssuedBookEdition");
                    Date Return_Date = rs.getDate("RETURN DATE");
                    String R_ReturnDate = String.valueOf(Return_Date);
                    byte[] BookImage = rs.getBytes("IssuedBookImage");

                    ImageIcon Format;
                    format = new ImageIcon(BookImage);
                    Image IMAGE = format.getImage().getScaledInstance(Return_Bimage.getWidth(), Return_Bimage.getHeight(), Image.SCALE_SMOOTH);
                    ImageIcon resizedImageIcon = new ImageIcon(IMAGE);

                    Return_Sid.setText(StudentId);
                    Return_Sname.setText(Sname);
                    Return_Sroll.setText(Sroll);
                    Return_Sstream.setText(Sstream);
                    Return_Sdept.setText(Sdepartment);
                    Return_Simage.setIcon(ResizedImageIcon);

                    Return_Bname.setText(bookname);
                    Return_Bpublisher.setText(bookpublisher);
                    Return_Bprice.setText(bookprice);
                    Return_Bedition.setText(bookedition);
                    Return_BissueDate.setText(R_IssueDate);
                    Return_BreturnDate.setText(R_ReturnDate);
                    Return_Bauthor.setText(bookauthor);
                    TodayDate.setText(ToDayDate);

                    LocalDate date1 = LocalDate.parse(Return_BreturnDate.getText(), DateTimeFormatter.ISO_DATE);
                    LocalDate date2 = LocalDate.parse(TodayDate.getText(), DateTimeFormatter.ISO_DATE);
                    long daysBetween = ChronoUnit.DAYS.between(date1, date2);
                    String Elapsedays = String.valueOf(daysBetween);

                    pst = con.prepareStatement(MyLibrarySettingQuery);
                    rs = pst.executeQuery();

                    while (rs.next()) {
                        int FineperDay = rs.getInt("FineperDays");
                        
                        Return_late.setText(Elapsedays);
                        if (daysBetween > 0) {
                            long Fine = daysBetween * FineperDay;
                            Return_Bfine.setText(String.valueOf(Fine));

                        } else {
                            Return_late.setText("0");
                            Return_Bfine.setText("0");
                        }

                    }
                    

                    Return_Bimage.setIcon(resizedImageIcon);
                    Return_Bname.setText(bookname);

                } while (rs.next());
            }
        } catch (SQLException ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Return_BidKeyReleased

