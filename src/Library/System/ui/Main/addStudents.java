    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            // TODO add your handling code here:
            studentid = Add_StudentId.getText();
            Studentname = Add_StudentName.getText();
            studentRoll = Add_StudentRoll.getText();
            studentstream = Add_StudentStream.getSelectedItem().toString();
            studentDepartment = Add_Studentdepartment.getText();
            studentImagePath = Add_StudentImageFilePath.getText();
            studentNickName = Add_StudentNickName.getText();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            date = sdf.format(Add_StudentDOB.getDate());
            studentGender = Add_StudentGender.getSelectedItem().toString();
            mobileNumber = Add_StudentContactNumber.getText();
            studentGmail = Add_StudentGmail.getText();
            studentAddress = Add_StudentAddress.getText();

            File StudentFile = new File(studentImagePath);
            String studentInsertSQLQuery;
            studentInsertSQLQuery = "INSERT INTO `student`(`id`, `Name`, `Roll`, `Stream`, `Department`, `student_imgPath`, `Student_Image`, `Nickname`, `DOB`, `Gender`, `Contact`, `Gmail`, `Address`, `STATUS`, `NoOfBookIssued`,`AddDate`) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
            String SelectQuery;
            SelectQuery = "SELECT  * FROM `student`";
            pst = con.prepareStatement(SelectQuery);
            rs = pst.executeQuery();

            while (rs.next()) {
                String STUDENTID = rs.getString("id");
                String ROLL = rs.getString("Roll");
                String PHONE = rs.getString("Contact");

                if (STUDENTID.equals(studentid)) {
                    JOptionPane.showMessageDialog(this, "STUDENT ID IS ALREADY EXIST", "ERROR", JOptionPane.ERROR_MESSAGE);
                    Add_StudentId.setText("");
                    Add_StudentId.requestFocus();
                    return;
                }
                if (ROLL.equals(studentRoll)) {
                    JOptionPane.showMessageDialog(this, "ROLL NO. IS  ALREADY EXIST", "ERROR", JOptionPane.ERROR_MESSAGE);
                    Add_StudentRoll.setText("");
                    Add_StudentRoll.requestFocus();
                    return;
                }
                if (PHONE.equals(mobileNumber)) {
                    JOptionPane.showMessageDialog(this, "PHONE NUMBER IS  ALREADY EXIST", "ERROR", JOptionPane.ERROR_MESSAGE);
                    Add_StudentContactNumber.setText("");
                    Add_StudentContactNumber.requestFocus();
                    return;
                }

            }
            if (mobileNumber.matches("^[0-9]*$") && mobileNumber.length() == 10) {

                Add_StudentContactNumber.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 255)));

            } else {

                Add_StudentContactNumber.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0), 3));
                Add_StudentContactNumber.requestFocus();
                return;
            }
            if (!(Pattern.matches("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$", Add_StudentGmail.getText()))) {

                Add_StudentGmail.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0), 3));
                Add_StudentGmail.requestFocus();
                return;
            }
            Date d1 = new Date();
            String ToDayDate = sdf.format(d1);

            InputStream StudentIS = new FileInputStream(StudentFile);
            pst = con.prepareStatement(studentInsertSQLQuery);
            pst.setString(1, studentid);
            pst.setString(2, Studentname);
            pst.setString(3, studentRoll);
            pst.setString(4, studentstream);
            pst.setString(5, studentDepartment);
            pst.setString(6, studentImagePath);
            pst.setBlob(7, StudentIS);
            pst.setString(8, studentNickName);
            pst.setString(9, date);
            pst.setString(10, studentGender);
            pst.setString(11, mobileNumber);
            pst.setString(12, studentGmail);
            pst.setString(13, studentAddress);
            pst.setString(14, Student_Status);
            pst.setInt(15, 0);
            pst.setString(16, ToDayDate);

            int k = pst.executeUpdate();

            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Student Added Successfully");
                Add_StudentId.setText("");
                Add_StudentName.setText("");
                Add_StudentRoll.setText("");
                Add_StudentStream.setSelectedIndex(-1);
                Add_Studentdepartment.setText("");
                Add_StudentImageFilePath.setText("");
                Label_Image.setIcon(null);
                Add_StudentNickName.setText("");
                Add_StudentDOB.setDate(null);
                Add_StudentGender.setSelectedIndex(-1);
                Add_StudentContactNumber.setText("");
                Add_StudentGmail.setText("");
                Add_StudentAddress.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "There Is some Error", "FAILED", JOptionPane.OK_OPTION);
            }
        } catch (SQLException ex) {
            Logger.getLogger(addStudents.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(addStudents.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Please Select student image");
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(this, "PLEASE FILL ALL DETAILS", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }


    }//GEN-LAST:event_jButton4ActionPerformed

    private void AddimgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddimgActionPerformed
        // TODO add your handling code here:
        try {
            JFileChooser imageFileChooser = new JFileChooser();
            int imageChooser = imageFileChooser.showOpenDialog(null);
            imageFileChooser.setDialogTitle("Choose Image...");
            //Only Choose "png" , "jpg" , "jpeg" files
            FileNameExtensionFilter fnef = new FileNameExtensionFilter("IMAGES", "png", "jpg", "jpeg");
            imageFileChooser.setFileFilter(fnef);
            if (imageChooser == JFileChooser.APPROVE_OPTION) {
                File imageFile = imageFileChooser.getSelectedFile();
                String imageFilePath = imageFile.getAbsolutePath();
                String imageFileName = imageFile.getName();

                //Display image path in text field
                Add_StudentImageFilePath.setText(imageFilePath);
                ImageIcon imageIcon = new ImageIcon(imageFilePath);
                //Resize Image to fit the JLabel
                Image image = imageIcon.getImage().getScaledInstance(Label_Image.getWidth(), Label_Image.getHeight(), Image.SCALE_SMOOTH);
                //Display Image on JLabel
                ImageIcon resizedImageIcon = new ImageIcon(image);
                Label_Image.setIcon(resizedImageIcon);
            }
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(this, "Please select JPG or PNG or JPEG file format", "Image Upload Failed", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_AddimgActionPerformed

