    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        try {
            // TODO add your handling code here:
            String UpdateBookQuery = "UPDATE `book` SET `BookcategoryID`=?,`BookcategoryName`=?,`BOOK NAME`=?,`AUTHOR`=?,`PUBLISHER`=?,`Bookprice`=?,`EDITION`=?,`BookPages`=?,`RACK`=?,`ROW`=?,`COLUMN`=?,`bookImg_path`=?,`BookImageName`=?,`Book_Image`=?WHERE `BOOK ID`=?";
            String SelectQuery = "SELECT  * FROM `book`";
            String bookId = txtBid.getText();
            CategoryItem citem = (CategoryItem) Edit_txtCategory.getSelectedItem();
            int DefaultCategory = Integer.parseInt(DefaultCategory_Id.getText());
            String DfaultCategoryName = Edit_Default_txtCategory.getText();
            String bookTitle = Edit_txtBname.getText();
            String bookbAuthor = Edit_txtBauthor.getText();
            String bookPublisher = Edit_txtBpublisher.getText();
            int Bprice = Integer.parseInt(Edit_txtBprice.getText());
            String bookEdition = Edit_txtBedition.getText();
            int bookPages = Integer.parseInt(Edit_txtBpages.getText());
            int B_Rack = Integer.parseInt(Edit_txtBrack.getText());
            int B_Row = Integer.parseInt(Edit_txtBrow.getText());
            int B_Column = Integer.parseInt(Edit_txtBcolumn.getText());
            String bImg_path = Edit_txtBimgPath.getText();
            String bImg_Name = Edit_txtBimgName.getText();

            pst = con.prepareStatement(SelectQuery);

            rs = pst.executeQuery();
            while (rs.next()) {

                String ID = rs.getString("BOOK ID");
                int Rack = rs.getInt("RACK");
                int Row = rs.getInt("ROW");
                int Column = rs.getInt("COLUMN");

                if (Rack == Integer.parseInt(Edit_txtBrack.getText()) && Row == Integer.parseInt(Edit_txtBrow.getText()) && Column == Integer.parseInt(Edit_txtBcolumn.getText())) {

                    JOptionPane.showMessageDialog(this, "Here is already a Book,Change Book Place", "WARNING", JOptionPane.WARNING_MESSAGE);
                    Edit_txtBrow.requestFocus();
                    return;
                }
            }

            if (Edit_txtCategory.isEnabled() == true) {
                File BooKFile = new File(bImg_path);
                InputStream BookIS = new FileInputStream(BooKFile);
                pst = con.prepareStatement(UpdateBookQuery);
                pst.setInt(1, citem.id);
                pst.setString(2, citem.name);
                pst.setString(3, bookTitle);
                pst.setString(4, bookbAuthor);
                pst.setString(5, bookPublisher);
                pst.setInt(6, Bprice);
                pst.setString(7, bookEdition);
                pst.setInt(8, bookPages);
                pst.setInt(9, B_Rack);
                pst.setInt(10, B_Row);
                pst.setInt(11, B_Column);
                pst.setString(12, bImg_path);
                pst.setString(13, bImg_Name);
                pst.setBlob(14, BookIS);
                pst.setString(15, bookId);

                int m = pst.executeUpdate();

                if (m == 1) {
                    JOptionPane.showMessageDialog(null, "Book Updated Successfully");
                    Edit_Default_txtCategory.setText("");
                    Edit_txtCategory.setVisible(false);
                    Edit_txtCategory.setSelectedIndex(-1);
                    txtBid.setText("");
                    Edit_txtBname.setText("");
                    Edit_txtBauthor.setText("");
                    Edit_txtBpublisher.setText("");
                    Edit_txtBprice.setText("");
                    Edit_txtBedition.setText("");
                    Edit_txtBpages.setText("");
                    Edit_txtBrack.setText("");
                    Edit_txtBrow.setText("");
                    Edit_txtBcolumn.setText("");
                    Edit_txtBimgPath.setText("");
                    Edit_txtBimgName.setText("");
                    Edit_txtBimg.setIcon(null);
                } else {
                    JOptionPane.showMessageDialog(null, "Book Updation Failed");
                }
            }
            if (Edit_txtCategory.isEnabled() == false) {
                File BooKFile = new File(bImg_path);
                InputStream BookIS = new FileInputStream(BooKFile);
                pst = con.prepareStatement(UpdateBookQuery);
                pst.setInt(1, DefaultCategory);
                pst.setString(2, DfaultCategoryName);
                pst.setString(3, bookTitle);
                pst.setString(4, bookbAuthor);
                pst.setString(5, bookPublisher);
                pst.setInt(6, Bprice);
                pst.setString(7, bookEdition);
                pst.setInt(8, bookPages);
                pst.setInt(9, B_Rack);
                pst.setInt(10, B_Row);
                pst.setInt(11, B_Column);
                pst.setString(12, bImg_path);
                pst.setString(13, bImg_Name);
                pst.setBlob(14, BookIS);
                pst.setString(15, bookId);

                int m = pst.executeUpdate();

                if (m == 1) {
                    JOptionPane.showMessageDialog(null, "Book Updated Successfully");
                    Edit_Default_txtCategory.setText("");
                    Edit_txtCategory.setVisible(false);
                    Edit_txtCategory.setSelectedIndex(-1);
                    txtBid.setText("");
                    Edit_txtBname.setText("");
                    Edit_txtBauthor.setText("");
                    Edit_txtBpublisher.setText("");
                    Edit_txtBprice.setText("");
                    Edit_txtBedition.setText("");
                    Edit_txtBpages.setText("");
                    Edit_txtBrack.setText("");
                    Edit_txtBrow.setText("");
                    Edit_txtBcolumn.setText("");
                    Edit_txtBimgPath.setText("");
                    Edit_txtBimgName.setText("");
                    Edit_txtBimg.setIcon(null);
                } else {
                    JOptionPane.showMessageDialog(null, "Book Updation Failed");
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(EditBookDetails.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(EditBookDetails.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(null, "You have to change the Book Category");
            return;
        }


    }//GEN-LAST:event_UpdateActionPerformed

