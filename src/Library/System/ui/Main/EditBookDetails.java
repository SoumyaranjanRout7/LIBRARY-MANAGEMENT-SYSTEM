    public void Category() {

        String st;
        st = "Active";
        try {
            pst = con.prepareStatement("SELECT * FROM `category` WHERE status=?");
            pst.setString(1, st);
            rs = pst.executeQuery();
            Edit_txtCategory.removeAllItems();

            while (rs.next()) {
                Edit_txtCategory.addItem(new CategoryItem(rs.getInt(1), rs.getString(2)));
            }

        } catch (SQLException ex) {
            Logger.getLogger(addStudents.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

