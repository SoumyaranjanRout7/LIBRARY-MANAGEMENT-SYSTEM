    private void txtPasswordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPasswordKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String query = "select * from login where username=? and password=?";
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/slibrary", "root", "");
                pst = con.prepareStatement(query);
                pst.setString(1, txtUser.getText());
                pst.setString(2, txtPassword.getText());
                rs = pst.executeQuery();
                if (rs.next()) {
                    Icon icon = new javax.swing.ImageIcon(getClass().getResource("/Library/System/ui/Main/project Images/Login.jpg"));
                    App_HomePage Home = new App_HomePage();
                    JOptionPane.showMessageDialog(null, "Login Successfully", "Authentication Success", JOptionPane.OK_OPTION, icon);

                    this.hide();
                    Home.setVisible(true);
                } else {
                    Icon icon = new javax.swing.ImageIcon(getClass().getResource("/Library/System/ui/Main/project Images/Login Fail.png"));
                    JOptionPane.showMessageDialog(null, "Invalid Username or Password", "Authentication Failed", JOptionPane.ERROR_MESSAGE, icon);
                    txtUser.setText("");
                    txtPassword.setText("");
                    txtUser.requestFocus();

                }
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Connection Error");

            }
        }
    }//GEN-LAST:event_txtPasswordKeyPressed

