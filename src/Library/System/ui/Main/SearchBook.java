    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        try {
            // TODO add your handling code here:
            String BOOkName = jTextField2.getText();
            int PRICE;
            int page;
            int rack;
            int row;
            int column;

            pst = con.prepareStatement("SELECT * FROM `book` WHERE `BOOK NAME` = ?");
            pst.setString(1, BOOkName);
            rs = pst.executeQuery();

            if (rs.next() == false) {
                JOptionPane.showMessageDialog(null, "BOOK NOT FOUND");
                return;

            } else {
                do {
                    String bookId = rs.getString("BOOK ID");
                    String bookcategory = rs.getString("BookcategoryName");
                    String bookauthor = rs.getString("AUTHOR");
                    String bookpublisher = rs.getString("PUBLISHER");
                    PRICE = rs.getInt("Bookprice");
                    String bookprice = String.valueOf(PRICE);
                    String bookedition = rs.getString("EDITION");
                    page = rs.getInt("BookPages");
                    String bookpages = String.valueOf(page);
                    rack = rs.getInt("RACK");
                    String bookrack = String.valueOf(rack);
                    row = rs.getInt("ROW");
                    String bookrow = String.valueOf(row);
                    column = rs.getInt("COLUMN");
                    String bookcolumn = String.valueOf(column);
                    BufferedImage im = ImageIO.read(rs.getBinaryStream("Book_Image"));

                    BID.setText(bookId);
                    Bcategory.setText(bookcategory);
                    BAuthor.setText(bookauthor);
                    BPublisher.setText(bookpublisher);
                    BPrice.setText(bookprice);
                    BEdition.setText(bookedition);
                    BPages.setText(bookpages);
                    BRack.setText(bookrack);
                    BRow.setText(bookrow);
                    BColumn.setText(bookcolumn);
                    ImageIcon format;
                    format = new ImageIcon(im);
                    Image image = format.getImage().getScaledInstance(SbyName_Bimg.getWidth(), SbyName_Bimg.getHeight(), Image.SCALE_SMOOTH);
                    ImageIcon resizedImageIcon = new ImageIcon(image);
                    SbyName_Bimg.setIcon(resizedImageIcon);

                } while (rs.next());
            }

        } catch (SQLException ex) {
            Logger.getLogger(SearchBook.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(SearchBook.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton4ActionPerformed

