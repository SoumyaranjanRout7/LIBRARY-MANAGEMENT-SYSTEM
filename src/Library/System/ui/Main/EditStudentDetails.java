    private void Edit_StudentIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Edit_StudentIdKeyReleased
        // TODO add your handling code here:
            try {
                // TODO add your handling code here:
                String StactusCheck1 = "ACTIVE";
                String StactusCheck2 = "INACTIVE";
                String Studentid = Edit_StudentId.getText();
                String SelectStudentQuery = "SELECT * FROM `student` WHERE `id` = ?";
                pst = con.prepareStatement(SelectStudentQuery);
                pst.setString(1, Studentid);

                rs = pst.executeQuery();

                if (rs.next() == false) {
                    return;
                } else {
                    do {
                        String Sname = rs.getString("Name");
                        String Sroll = rs.getString("Roll");
                        String Sstream = rs.getString("Stream");
                        String Sdepartment = rs.getString("Department");
                        String ImgPath = rs.getString("student_imgPath");
                        byte[] EditStudent_img = rs.getBytes("Student_Image");
                        String SnickName = rs.getString("Nickname");
                        Date Sdob = rs.getDate("DOB");
                        String Sgender = rs.getString("Gender");
                        String SContact = rs.getString("Contact");
                        String SGmail = rs.getString("Gmail");
                        String SAddress = rs.getString("Address");
                        String status = rs.getString("STATUS");
                        ImageIcon format;
                        format = new ImageIcon(EditStudent_img);
                        Image image = format.getImage().getScaledInstance(EditStudent_Img.getWidth(), EditStudent_Img.getHeight(), Image.SCALE_SMOOTH);
                        ImageIcon resizedImageIcon = new ImageIcon(image);

                        if (status.equals(StactusCheck1)) {
                            Edit_StudentName.setText(Sname);
                            Edit_StudentRoll.setText(Sroll);
                            Edit_StudentStream.setText(Sstream);
                            Edit_StudentDepartment.setText(Sdepartment);
                            S_imgPath.setText(ImgPath);
                            EditStudent_Img.setIcon(resizedImageIcon);
                            Edit_StudentNickName.setText(SnickName);
                            Edit_Studentdob.setDate(Sdob);
                            Edit_StudentGender.setSelectedItem(Sgender);
                            Edit_StudentContactNumber.setText(SContact);
                            Edit_StudentGmail.setText(SGmail);
                            Edit_StudentAddress.setText(SAddress);

                            Edit_StudentStream.setEditable(true);
                            Edit_StudentName.setEditable(true);
                            Edit_StudentRoll.setEditable(true);
                            Edit_StudentStream.setEditable(true);
                            Edit_StudentDepartment.setEditable(true);
                            S_imgPath.setEditable(true);
                            Edit_StudentNickName.setEditable(true);
                            Edit_Studentdob.setEnabled(true);
                            Edit_StudentGender.setEditable(true);
                            Edit_StudentContactNumber.setEditable(true);
                            Edit_StudentGmail.setEditable(true);
                            Edit_StudentAddress.setEditable(true);
                            jButton4.setEnabled(true);
                            jButton1.setEnabled(true);
                            jButton6.setEnabled(true);
                        }
                        if (status.equals(StactusCheck2)) {
                            Edit_StudentName.setText(Sname);
                            Edit_StudentRoll.setText(Sroll);
                            Edit_StudentStream.setText(Sstream);
                            Edit_StudentDepartment.setText(Sdepartment);
                            S_imgPath.setText(ImgPath);
                            EditStudent_Img.setIcon(resizedImageIcon);
                            Edit_StudentNickName.setText(SnickName);
                            Edit_Studentdob.setDate(Sdob);
                            Edit_StudentGender.setSelectedItem(Sgender);
                            Edit_StudentContactNumber.setText(SContact);
                            Edit_StudentGmail.setText(SGmail);
                            Edit_StudentAddress.setText(SAddress);
                            JOptionPane.showMessageDialog(null, "Student is Inactive So you can't Edit");
                            Edit_StudentName.setEditable(false);
                            Edit_StudentRoll.setEditable(false);
                            Edit_StudentStream.setEditable(false);
                            Edit_StudentDepartment.setEditable(false);
                            S_imgPath.setEditable(false);
                            Edit_StudentNickName.setEditable(false);
                            Edit_Studentdob.setEnabled(false);
                            Edit_StudentGender.setEnabled(false);
                            Edit_StudentContactNumber.setEditable(false);
                            Edit_StudentGmail.setEditable(false);
                            Edit_StudentAddress.setEditable(false);
                            jButton4.setEnabled(false);
                            jButton1.setEnabled(false);
                            jButton6.setEnabled(false);
                        }

                    } while (rs.next());
                }

            } catch (SQLException ex) {
                Logger.getLogger(EditStudentDetails.class.getName()).log(Level.SEVERE, null, ex);
            }

        
    }//GEN-LAST:event_Edit_StudentIdKeyReleased

